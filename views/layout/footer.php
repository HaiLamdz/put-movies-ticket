<script>
    const trailerLink = document.getElementById('trailerLink');
    const trailerContainer = document.getElementById('trailerContainer');
    const overlay = document.getElementById('overlay');

    // Đặt trailer_url từ PHP vào một biến JavaScript
    const trailerUrl = '<iframe width="900" height="500" src="<?= $detailMovies['trailer_url'] ?>" title="VENOM: KÈO CUỐI - Trailer | KC: 25.10.2024" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share" referrerpolicy="strict-origin-when-cross-origin" allowfullscreen></iframe>'; // đảm bảo giá trị này là một URL hợp lệ

    $(trailerLink).click(function(e) {
        e.preventDefault();

        // Hiển thị overlay và video
        overlay.style.display = "block";
        trailerContainer.innerHTML = trailerUrl;
        trailerContainer.style.display = "block";

    });

    // Thêm sự kiện cho overlay để ẩn video và overlay
    overlay.onclick = function() {
        overlay.style.display = "none"; // Ẩn overlay
        trailerContainer.innerHTML = ""; // Dừng video
        trailerContainer.style.display = "none"; // Ẩn video container
    };
</script>
